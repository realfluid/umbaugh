				<span class="date">By Paige Gregory, CPA, Manager<br /> August 4, 2010 <a href="#">Newsletter</a> </span>
                    <p>During the budget preparation season you will often hear budget myths and misconceptions in addition to the common questions we answered in the last issue of Footnotes.  This article helps you sort out the myths from the facts as you prepare your 2011 budget.</p>
                    <p><strong>Myth #1: The DLGF keeps cutting my budget.</strong></p>
                    <p>Fact: The Department of Local Government Finance (the DLGF) does not exist to arbitrarily make budget cuts to taxing units in Indiana.  The purpose of the DLGF is to make sure everyone follows the law regarding local government funding and property tax assessment.  If a taxing unit cannot afford the budget it's proposing, it should not be a surprise when the 1782 Notice and Budget Order come back with budget reductions.  Especially in this time of circuit breaker credits and decreasing revenues, each taxing unit should know when there will be a temporary cash flow shortage or a structural budget deficit and react accordingly.  A monthly cash flow and forward-looking projections are highly recommended financial planning tools to prevent being surprised by a 1782 Notice and Budget Order.</p>
                    <p><strong>Myth #2: Use it, encumber it or lose it.</strong></p>
                    <p>Fact: A DLGF-approved budget is permission to spend.  When a taxing unit receives an order with an approved budget, this approved amount does not appear in the checking account of the taxing unit.  It is the maximum amount that may be spent without additional permission from the council, board or the DLGF.  In a personal bank account, money that is not spent stays in the account.  This is also true of taxing units.  If an appropriation is not spent, the money stays in the bank account to fund a cash balance or another appropriation.  The appropriation will expire at the end of the budget year, but there will be a new one at the beginning of the next budget year.  Appropriations may be encumbered only through a purchase order or contract.</p>
                    <p><strong>Myth #3: Budget to the max to get the max.</strong></p>
                    <p>Fact: Simply put, the maximum levy isn't contingent upon a taxing unit's budget; it is based on a statutory calculation performed by the DLGF.  This means the DLGF does not take budget estimates into account when calculating a maximum levy.  Make your budget estimates realistic, and base them on what your unit can actually afford.  However, it is important to note that the current statutory limitation encourages taxing units to set a high tax levy and tax rate to get the maximum levy.</p>
                    <p>Given the complexities and changes, it is always a good idea to have someone take a second look at your budget before its adoption.  If you have additional questions or need assistance in preparing your budget, please contact us.</p>
